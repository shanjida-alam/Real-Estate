    public void publishData() {
        try {
            model.setRowCount(0);
            pst = con.prepareStatement("SELECT * FROM owner;");
            rs = pst.executeQuery();
            String id = "";
            String name = "";
            String mob = "";
            String mail = "";
            String add = "";
            while (rs.next()) {
                id = rs.getString(1);
                name = rs.getString(2);
                mob = rs.getString(3);
                mail = rs.getString(4);
                add = rs.getString(5);
                model.insertRow(model.getRowCount(), new Object[]{id, name, mob, mail, add});
            }
        } catch (Exception e) {

        }
    }

